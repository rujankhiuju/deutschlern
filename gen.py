# Parse new cards
def parse_new(tup):
    tc = tup[0]
    if tc == "adj": typ = "adjective"
    elif tc == "prep": typ = "preposition"
    elif tc == "conj": typ = "conjunction"
    elif tc == "pron": typ = "pronoun"
    elif tc == "t": typ = "time"
    elif tc == "col": typ = "color"
    elif tc == "num": typ = "number"
    else: typ = TYPE_MAP.get(tc, tc)
    
    c = {"id": tup[2], "level": tup[1], "type": typ, "german": tup[3], "english": tup[4]}
    if typ == "noun":
        c["article"] = tup[5]
        c["plural"] = tup[6]
        c["emoji"] = tup[7]
        if len(tup) > 8 and tup[8] == 1: c["exam"] = True
    elif typ == "verb":
        c["pattern"] = tup[5]
        c["emoji"] = tup[6]
        if len(tup) > 7 and tup[7] == 1: c["exam"] = True
    else:
        c["emoji"] = tup[5]
        if len(tup) > 6 and tup[6] == 1: c["exam"] = True
    return c

